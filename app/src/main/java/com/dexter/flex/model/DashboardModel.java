package com.dexter.flex.model;
public class DashboardModel {

    private String reach_time;
    private String running_number;
    private String uid;
    private String random;
    private String vehicle_model;
    private String vehicle_type;
    private String washing_status;

    public DashboardModel() {
    }

    public String getReach_time() {
        return reach_time;
    }

    public void setReach_time(String reach_time) {
        this.reach_time = reach_time;
    }

    public String getRunning_number() {
        return running_number;
    }

    public void setRunning_number(String running_number) {
        this.running_number = running_number;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getRandom() {
        return random;
    }

    public void setRandom(String random) {
        this.random = random;
    }

    public String getVehicle_model() {
        return vehicle_model;
    }

    public void setVehicle_model(String vehicle_model) {
        this.vehicle_model = vehicle_model;
    }

    public String getVehicle_type() {
        return vehicle_type;
    }

    public void setVehicle_type(String vehicle_type) {
        this.vehicle_type = vehicle_type;
    }

    public String getWashing_status() {
        return washing_status;
    }

    public void setWashing_status(String washing_status) {
        this.washing_status = washing_status;
    }
}